
public class keyValuePair {
	
	public String key;
	public String value;
	
	//Empty Constructor
	public keyValuePair()
	{
		this.key = null;
		this.value = null;
	}
	
	/**
	 * Parameterized Constructor
	 * @param key
	 * @param value
	 */
	public keyValuePair(String key, String value)
	{
		this.key = key;
		this.value = value;
	}
	
}
